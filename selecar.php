         <p>Un nuevo hito logró el deporte argentino. Esta vez, los protagonistas fueron la selección argentina de taekwondo songahm se consagró campeona del mundo en la categoría sparring -modalidad lucha- en el torneo que se llevó a cabo en Phoenix, Arizona, Estados Unidos.
        El pasado viernes 14, el conjunto nacional comandado por el chaqueño senior Master Agustin Spiess, venció al equipo de EEUU para conquistar por primera vez en la historia la medalla dorada. Fueron 12 los argentinos que participaron de este logro, en una evento en el que además, cada taekwondista rindió los exámenes pertinentes para mejorar en su clasificación de danes.<br>
        Otro de los representantes del equipo fue Mauro Echazarreta, que una vez que el seleccionado alcanzó el título, expresó su felicidad por la inédita conquista. “Este logro obtenido es inédito e histórico para el taekwondo argentino, ya que nunca antes se había podido conseguir”, expresó en diálogo con la Voz de Chaco.
        Es importante destacar que, en la edición 2022 del torneo, el combinado nacional de taekwondo ATA ya se había subido al podio tras quedarse con la medalla de plata en la modalidad combat weapons. Además, terminaron en el cuarto lugar de la modalidad traditional sparring.</p>
