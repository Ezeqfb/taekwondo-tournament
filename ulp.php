    <div class= "parrafo-noticia">
        <p>La profesora Micaela Palacios, árbitra y entrenadora de la escuela de taekwondo del Campus ULP, dio una charla “de introducción al reglamento de arbitraje del área de Kyorugui”, la modalidad de combate de este arte marcial, comentó Micaela.
        Participaron tanto atletas como entrenadores de la provincia, y la intención es “despertar el interés en el arbitraje, ya que dar a conocer el reglamento favorece las estrategias a la hora de una competencia. También la idea es ir formando árbitros provinciales y nacionales”, argumentó la profesional.<br>
        En esta primera jornada se desarrolló un panorama general y habrá una nueva fecha para septiembre, donde se verán puntos más específicos.
        Micaela indicó que “finalmente serán evaluadas las personas que quieran dedicarse al arbitraje de taekwondo y con esto formar un cuerpo de árbitros de la Federación de San Luis”.</p>
